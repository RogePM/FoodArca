import * as React from "react"
import { cn } from "@/lib/utils" // Assuming you have a utils file for merging classes

const Progress = React.forwardRef(({ className, value, indicatorColor, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "relative h-2 w-full overflow-hidden rounded-full bg-gray-100",
      className
    )}
    {...props}
  >
    <div
      className={cn("h-full w-full flex-1 transition-all duration-500 ease-in-out", indicatorColor || "bg-gray-900")}
      style={{ transform: `translateX(-${100 - (value || 0)}%)` }}
    />
  </div>
))
Progress.displayName = "Progress"

export { Progress }